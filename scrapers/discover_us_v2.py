def build_candidates():
    candidates = []
    seen = set()
    for cat, domains in BRANDS.items():
        for d in domains:
            d = d.strip().lower().replace(" ", "")
            if d not in seen and "." in d and not d.startswith("."):
                seen.add(d)
                candidates.append({
                    "domain": d,
                    "category": cat,
                    "source": "curated"
                })
    return candidates

def validate_candidate(c):
    domain = c["domain"]
    result = {"domain": domain, "category": c["category"], "source": c["source"]}

    # Try Shopify first (most common for DTC)
    is_shopify, info = probe_shopify(domain)
    if is_shopify is None:
        result["platform"] = None
        result["status"] = "rate_limited"
        return result
    if is_shopify:
        result["platform"] = "shopify"
        result["status"] = "valid"
        result["product_count"] = info
        return result

    # Try WooCommerce
    is_wc = probe_woocommerce(domain)
    if is_wc is None:
        result["platform"] = None
        result["status"] = "rate_limited"
        return result
    if is_wc:
        result["platform"] = "woocommerce"
        result["status"] = "valid"
        return result

    # Try BigCommerce
    is_bc = probe_bigcommerce(domain)
    if is_bc is None:
        result["platform"] = None
        result["status"] = "rate_limited"
        return result
    if is_bc:
        result["platform"] = "bigcommerce"
        result["status"] = "valid"
        return result

    # Try Magento
    is_mg = probe_magento(domain)
    if is_mg is None:
        result["platform"] = None
        result["status"] = "rate_limited"
        return result
    if is_mg:
        result["platform"] = "magento"
        result["status"] = "valid"
        return result

    result["platform"] = "unknown"
    result["status"] = "failed"
    return result

def main():
    candidates = build_candidates()
    print(f"Candidates: {len(candidates)}")

    valid = {"shopify": [], "woocommerce": [], "bigcommerce": [], "magento": []}
    rate_limited = []
    failed = []

    # Use moderate concurrency to avoid rate limiting
    workers = 20
    print(f"Probing with {workers} workers...")
    with concurrent.futures.ThreadPoolExecutor(max_workers=workers) as ex:
        futures = {ex.submit(validate_candidate, c): c for c in candidates}
        for i, f in enumerate(concurrent.futures.as_completed(futures)):
            result = f.result()
            status = result["status"]
            platform = result.get("platform")

            if status == "rate_limited":
                rate_limited.append(result)
            elif status == "valid" and platform:
                valid[platform].append(result)
            else:
                failed.append(result)

            if (i + 1) % 200 == 0:
                total_valid = sum(len(v) for v in valid.values())
                print(f"  Progress: {i+1}/{len(candidates)} | valid={total_valid} | "
                      f"rl={len(rate_limited)} | failed={len(failed)}")

    # Print summary
    total_valid = sum(len(v) for v in valid.values())
    total = len(candidates)
    print(f"\n{'='*60}")
    print(f"DISCOVERY SUMMARY")
    print(f"{'='*60}")
    print(f"Total candidates:     {total}")
    print(f"Valid merchants:      {total_valid} ({total_valid/total*100:.1f}%)")
    print(f"  Shopify:            {len(valid['shopify'])}")
    print(f"  WooCommerce:        {len(valid['woocommerce'])}")
    print(f"  BigCommerce:        {len(valid['bigcommerce'])}")
    print(f"  Magento:            {len(valid['magento'])}")
    print(f"Rate limited:         {len(rate_limited)}")
    print(f"Failed (no platform): {len(failed)}")

    # Category distribution for Shopify
    if valid["shopify"]:
        cats = {}
        for v in valid["shopify"]:
            cats[v["category"]] = cats.get(v["category"], 0) + 1
        print(f"\nShopify category distribution:")
        for c, n in sorted(cats.items(), key=lambda x: -x[1]):
            print(f"  {c:30} {n:>4}")

    # Save results
    timestamp = time.strftime("%Y%m%d_%H%M%S")

    # Save all Shopify merchants
    shopify_merchants = []
    for v in valid["shopify"]:
        shopify_merchants.append({
            "domain": v["domain"],
            "platform": "shopify",
            "country": "US",
            "currency": "USD",
            "category": v["category"],
            "product_count": v.get("product_count", 0),
            "source_attribution": "curated",
            "discovered_at": timestamp
        })

    with open(DATA / "us_shopify_merchants.json", "w") as f:
        json.dump({
            "description": "US Shopify merchant catalog — BUY-11037",
            "count": len(shopify_merchants),
            "merchants": shopify_merchants,
            "generated_at": timestamp
        }, f, indent=2)
    print(f"\nSaved: data/us_shopify_merchants.json ({len(shopify_merchants)} merchants)")

    # Save non-Shopify merchants
    non_shopify = []
    for platform in ["woocommerce", "bigcommerce", "magento"]:
        for v in valid[platform]:
            non_shopify.append({
                "domain": v["domain"],
                "platform": platform,
                "country": "US",
                "currency": "USD",
                "category": v["category"],
                "source_attribution": "curated",
                "discovered_at": timestamp
            })

    with open(DATA / "us_non_shopify_merchants.json", "w") as f:
        json.dump({
            "description": "US non-Shopify merchant catalog — BUY-11037",
            "count": len(non_shopify),
            "merchants": non_shopify,
            "generated_at": timestamp
        }, f, indent=2)
    print(f"Saved: data/us_non_shopify_merchants.json ({len(non_shopify)} merchants)")

    # Save rate-limited domains for retry
    with open(DATA / "rate_limited_domains.json", "w") as f:
        json.dump({
            "description": "Rate-limited domains for retry — BUY-11037",
            "count": len(rate_limited),
            "domains": rate_limited,
            "generated_at": timestamp
        }, f, indent=2)
    print(f"Saved: data/rate_limited_domains.json ({len(rate_limited)} domains)")

    # Save full report
    report = {
        "generated_at": timestamp,
        "total_candidates": total,
        "valid": {k: len(v) for k, v in valid.items()},
        "rate_limited": len(rate_limited),
        "failed": len(failed),
        "total_valid": total_valid
    }
    with open(DATA / "discovery_report.json", "w") as f:
        json.dump(report, f, indent=2)
    print(f"Saved: data/discovery_report.json")

    return report

